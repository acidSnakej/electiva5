    <h3>Historia del  baloncesto</h3>
    <h4>El baloncesto nació como una idea a la necesidad de realizar alguna actividad deportiva durante el invierno.
     Al profesor de la Universidad de Illinois (Massachusetts) James Naismith, (un profesor de educación física) le fue
     encargada la misión, en 1891, de idear un deporte que se pudiera jugar bajo techo, pues los inviernos en esa zona 
     dificultaban la realización de alguna actividad al aire libre.</h4>

    <p><h4>es un deporte de equipo que se puede desarrollar tanto en pista cubierta 
    como en descubierta, en el que dos conjuntos de cinco jugadores cada uno, intentan anotar 
    puntos, también llamados canastas o dobles y/o triples introduciendo un balón en un aro colocado
    a 3,05 metros del suelo del que cuelga una red, lo que le da un aspecto de cesta o canasta.</h4></p>
    <h3>Reglas de juego</h3>

    <h4> JUEGO DEL BALONCESTO :El baloncesto lo juegan dos (2) equipos de cinco (5) jugadores cada uno. 
    El objetivo de cada equipo es introducir el balón dentro de la canasta del adversario.</h4>
    <h4>MOVIMIENTO DEL BALÓN: El balón puede ser pasado, lanzado, palmeado rodado o botado en cualquier dirección
    dentro de las restricciones de los artículos pertinentes de las Reglas.</h4>
    <h4>GANADOR DE UN PARTIDO: El equipo que  mayor número de puntos al final del tiempo de juego del cuarto periodo
     o, si fuera necesario, de uno o más períodos extra, será el ganador del partido.</h4>
     <h3>Medidas de la cancha de baloncesto</h3>
     <h4>
        Una pista de baloncesto tiene que ser una superficie dura, plana, rectangular y libre de obstáculos, con 15 m de ancho y 28 m de longitud.
        El perímetro de la pista debe estar libre de obstáculos a dos metros de distancia.</h4>

      <h4> La altura del primer obstáculo que se encuentre verticalmente sobre la pista debe de estar como mínimo a 7 m de altura.</h4> 
      <h4> El campo está dividido en dos mitades iguales separados por la línea denominada de medio campo y con un círculo que parte 
      del centro de la pista, el círculo central mide 3,6 m de diámetro. Para cada equipo, el medio campo que contiene la canasta que se 
      defiende se denomina medio campo defensivo y el medio campo que contiene la canasta en la que se pretende anotar se denomina medio campo ofensivo.
        En los lados menores se sitúan los aros que están a 3,05 m de altura y se introducen a 1,20 m dentro del rectángulo y tienen que estar provistos de basculantes homologados.
        Paralela a la línea de fondo encontramos la línea de tiros libres, que se encuentra a 5,80 m de la línea de fondo y a 4,60 m de la canasta. El círculo donde se encuentra la línea de tiros libres tiene un diámetro de 3,6 m. Todas las líneas miden 5 cm. de ancho.
        La línea de tres puntos se encuentra situada a 6,75 m (FIBA) y a 7,24 m (NBA) de distancia de la canasta.</h4>
