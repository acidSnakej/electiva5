    <h3>Origen del termino TEAKWONDO</h3>
    <h4>La palabra taekwondo proviene de los caracteres Hanja que significan:

     <h4> (tae, escrito 태 en hangul): Técnicas que impliquen el uso de los pies. (patadas).</h4>
      <h4> (kwon): Técnicas que impliquen el uso de los brazos (puño, hombro, mano).</h4> 
      <h4> (do): Camino de perfeccionamiento. Concepto filosófico oriental, conocido también 
        como dao o tao en chino y como dō en japonés. </h4> 

        <h4>Por tanto, la palabra "Taekwondo" podría 
        traducirse como «El camino de pies y manos», nombre que hace referencia a que es un estilo 
        de combate que utiliza únicamente los pies, las manos y otras partes del cuerpo (como por 
        ejemplo las rodillas y los codos), prescindiendo por completo del uso de armas, tanto tradicionales 
        como modernas. Aunque diversos canales de Televisión han señalado que realmente la traducción 
        directa seria el «El Poder del Puño y la Patada» no están totalmente en lo correcto. Se considera un 
        método que busca acondicionar el cuerpo y activarlo sin que surja ningún freno, además del desarrollo 
        de la voluntad y la sabiduría. Basándose en las leyes físicas para generar la máxima potencia enfocada 
        de manera precisa, mediante la aceleración de la masa corporal en un gesto motor o grupo de estos en combinación.</h4>
    <p><h4>La federacion Mundial de Taekwondo (en ingles, World Taekwondo Federation, o WTF) 
    es la institucion que se dedica a regular las normas del taekwondo a nivel competitivo, asi como
    celebrar periodicamente competiciones y eventos en cada una de sus disciplinas. Es una entidad reconocida
 	por el Comite Olimpico Internacional.
	¿Que es taekwondo? Es un arte marcial transformado en deporte olimpico de combate desde el año 1988, fue 
	introducido como deporte demostrativo en los juegos olimpicos realizados en la ciudad de Seul en Corea del Sur.
	El pais de origen es Corea. </h4></p>

