    <h3>Historia de la natación</h3>
    <h4>En la era moderna la natación de competición se instituyó en Gran Bretaña a finales del
     siglo XVIII. La primera organización de este tipo fue la National Swimming Society, fundada 
     en Londres en 1837. En 1869 se creó la Metropolitan Swimming Clubs Association, que después
      se convirtió en la Amateur Swimming Association (ASA).</h4>
    <p><h4>La natación es el movimiento y el desplazamiento a través del agua mediante el uso
    de las extremidades corporales y por lo general sin utilizar ningún instrumento artificial o apoyo 
    para nadar. La natación es una actividad útil y recreativa para las personas, es beneficiosa para la 
    salud tanto a nivel físico como psíquico, es uno de los ejercicios físicos más completos para trabajar 
    la mente y el cuerpo, mantiene en forma, fortalece los músculos y la memoria, por lo que es recomendable 
    su práctica a cualquier edad.</h4></p>

